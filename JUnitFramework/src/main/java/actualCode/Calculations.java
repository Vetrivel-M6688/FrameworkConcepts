package actualCode;

public class Calculations {

	public int addition(int val1, int val2) {
		return val1 + val2;
	}

	public int subtraction(int num1, int num2) {
		int result = num1 - num2;
		return result;
	}
	
	public int multiply(int num1, int num2) {
		return num1*num2;
	}
	
	public int divide(int val1, int val2) {
		return val1/val2;
	}
}
